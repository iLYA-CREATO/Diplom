    public void OnTriggerEnter(Collider other)
    {
        if (other.tag == "Gem")
        {
            Debug.Log("NPC gem up");
            other.gameObject.SetActive(false);
            gemLocate.gemLocations.RemoveAll(item => item == null);
            GemNPC++;
        }
    }


